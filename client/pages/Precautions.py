import streamlit as st

st.title("Precautions for Heart Attack")

st.write("Taking care of your heart health is crucial to prevent heart diseases. Here are some effective precautions and lifestyle changes you can make:")

st.header("1. Regular Exercise")
st.write("Engage in regular physical activity such as brisk walking, jogging, swimming, or cycling. Aim for at least 150 minutes of moderate-intensity aerobic exercise every week.")

st.header("2. Balanced Diet")
st.write("Consume a diet rich in fruits, vegetables, whole grains, lean proteins, and healthy fats. Limit your intake of saturated and trans fats, cholesterol, salt, and added sugars.")

st.header("3. Healthy Weight")
st.write("Maintain a healthy weight through a combination of a balanced diet and regular exercise. Being overweight or obese increases the risk of heart disease.")

st.header("4. No Smoking")
st.write("If you smoke, quit as soon as possible. Smoking is a major risk factor for heart disease. Seek support from healthcare professionals to help you quit.")

st.header("5. Moderate Alcohol Consumption")
st.write("If you consume alcohol, do so in moderation. For men, this means up to two drinks per day, and for women, up to one drink per day.")

st.header("6. Stress Management")
st.write("Practice stress-reduction techniques such as meditation, deep breathing, yoga, or spending time on hobbies. Chronic stress can contribute to heart problems.")

st.header("7. Regular Health Check-ups")
st.write("Schedule regular check-ups with your healthcare provider. Monitoring your blood pressure, cholesterol levels, and overall health is essential for early detection and management of heart issues.")

st.header("8. Know Your Family History")
st.write("Understand your family's history of heart diseases. This information can help your healthcare provider assess your risk and recommend appropriate preventive measures.")

st.write("Remember, prevention is the key to a healthy heart. By adopting these precautions, you can significantly reduce your risk of heart disease.")

st.markdown("For more detailed and personalized information, **consult your doctor** and perform **annual health check-ups**.")

